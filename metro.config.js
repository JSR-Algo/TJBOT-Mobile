const fs = require('fs');
const path = require('path');

/**
 * Load .env file and write a generated __env__.ts that gets bundled.
 * This is the most reliable env injection approach for React Native CLI.
 */
function loadEnv() {
  const envPath = path.resolve(__dirname, '.env');
  if (!fs.existsSync(envPath)) return {};
  const lines = fs.readFileSync(envPath, 'utf-8').split('\n');
  const env = {};
  for (const line of lines) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith('#')) continue;
    const idx = trimmed.indexOf('=');
    if (idx < 0) continue;
    const key = trimmed.slice(0, idx).trim();
    const val = trimmed.slice(idx + 1).trim().replace(/^["']|["']$/g, '');
    env[key] = val;
  }
  return env;
}

const env = loadEnv();

// Write src/__env__.ts so it can be imported by src/config.ts
const envTs = `// AUTO-GENERATED by metro.config.js — do not edit manually
export const ENV = {
  TBOT_API_URL: ${JSON.stringify(env.TBOT_API_URL || '')},
  TBOT_AI_URL: ${JSON.stringify(env.TBOT_AI_URL || '')},
  EXPO_PUBLIC_GEMINI_LIVE_MODEL: ${JSON.stringify(env.EXPO_PUBLIC_GEMINI_LIVE_MODEL || '')},
  EXPO_PUBLIC_SENTRY_DSN: ${JSON.stringify(env.EXPO_PUBLIC_SENTRY_DSN || '')},
  EXPO_PUBLIC_POSTHOG_API_KEY: ${JSON.stringify(env.EXPO_PUBLIC_POSTHOG_API_KEY || '')},
  EXPO_PUBLIC_POSTHOG_HOST: ${JSON.stringify(env.EXPO_PUBLIC_POSTHOG_HOST || '')},
};
`;
const envTsPath = path.resolve(__dirname, 'src/__env__.ts');
fs.writeFileSync(envTsPath, envTs);

if (env.TBOT_API_URL || env.TBOT_AI_URL || env.EXPO_PUBLIC_GEMINI_LIVE_MODEL) {
  console.log('[metro] Injecting env vars from .env:');
  if (env.TBOT_API_URL) console.log(`  TBOT_API_URL = ${env.TBOT_API_URL}`);
  if (env.TBOT_AI_URL) console.log(`  TBOT_AI_URL  = ${env.TBOT_AI_URL}`);
  if (env.EXPO_PUBLIC_GEMINI_LIVE_MODEL) console.log(`  EXPO_PUBLIC_GEMINI_LIVE_MODEL = ${env.EXPO_PUBLIC_GEMINI_LIVE_MODEL}`);
}

